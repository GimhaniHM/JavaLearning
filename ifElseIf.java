public class ifElseIf {
    public static void main(String[] args){
        int x = 12 ;
        if(x<0){
            System.out.println("\nNegative number");
        }else if(x>0){
            System.out.println("\nPositive number");
        }else{
            System.out.println("\nZero");
        }
    }
}
